#This function multiplies coordinates by factor. I choose 1000 to have it +- like BSO examples

def scale_coordinates(data, factor):
    """
    Scale all coordinate values in a nested list structure by a given factor.

    Parameters:
        data (list): The original nested list structure containing coordinates and other values.
        factor (int or float): The scaling factor to apply to the coordinates.

    Returns:
        list: A new nested list structure with scaled coordinates.
    """

    def scale_element(element):
        if isinstance(element, list) and all(isinstance(x, (int, float)) for x in element):
            # Scale coordinates (pairs of numbers)
            return [x * factor for x in element]
        elif isinstance(element, list):
            # Recurse for nested lists
            return [scale_element(sub_element) for sub_element in element]
        else:
            # Keep non-list elements unchanged
            return element

    return [scale_element(item) for item in data]


